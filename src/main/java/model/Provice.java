package model;

public class Provice {
    private int id;
    private String name;

    public Provice() {
    }

    public Provice(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
